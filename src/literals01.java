public class literals01 {
    public static void main(String[] args) 
    {
        //literals
        
        //Hexa-decimal-number
        int num1 = 0x7E;
        System.out.println(num1);
        
        //to differentaite numbers
        int num2 = 10_000_000;
        System.out.println(num2);

        //epsolon

        double num3 = 12e10;
        System.out.println(num3);

        //char

        char c = 'a';
        System.out.println(c);

        //char increment
        char d ='a';
        d++;
        System.out.println(d);
    }
    
    
}
